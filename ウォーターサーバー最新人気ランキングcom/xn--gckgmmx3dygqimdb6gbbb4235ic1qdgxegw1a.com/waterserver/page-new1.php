<div class="point1">お水のタイプは2種類。「天然水」か「RO水」か</div>
「<strong>天然水</strong>」とは全国各地にある地下水や川などの水源から採水して、成分を損なわないように最低限の殺菌処理をしたお水です。採水地は富士山が多いですが、北アルプス、南阿蘇など水源は各社様々です。<span style="color:#FF0A0A"><strong>味や口当たりの良さなど自然本来のお水のおいしさを味わいたい方におすすめ</strong></span>です。RO水よりも価格は高め。<br />
<br />
「<strong>RO水</strong>」は水道水または天然水からROろ過という技術で不純物を可能な限りろ過した純水（ピュアウォーター）です。その後、ミネラルを添加して味を調整することもあります。<span style="color:#FF0A0A"><strong>価格は天然水の約2/3のため、お水の代金を安く抑えたい方におすすめ</strong></span>です。<br />
