<br />
<div class="point3">赤ちゃんが飲んでも安心かどうか</div>
お水には硬度があり「<strong>硬水</strong>」と「<strong>軟水</strong>」に分けられますが、硬度はお水に含まれるミネラル量で決まります。<br />
<br />
<strong>硬水</strong>はミネラルが多く、ミネラルは赤ちゃんの内臓に負担がかかるため、おすすめできません。<strong>軟水（=硬度100mg/L以下）</strong>はミネラルが少ないため、赤ちゃんのミルク作りや離乳食に使っても安心です。日本人が飲みやすくおいしいと感じるお水も軟水です。<br />
<br />
当サイトで紹介しているウォーターサーバーはすべて「<strong><span style="color:#FF0A0A">軟水</span></strong>」なので、<span style="color:#FF0A0A"><u><strong>どのメーカーを選んでいただいても赤ちゃんや小さなお子様に安心のお水</strong></u></span>です。<br />
<br />
<div class="point4">ボトルが「ワンウェイ方式」か「リターナブル方式」か</div>
「<strong>ワンウェイ方式</strong>」は使い捨てボトルなので、空ボトルの保管が不要で、リサイクルゴミとして処分が可能です。毎回新品の使い切りボトルが届くので衛生的です。<br />
<br />
「<strong>リターナブル方式</strong>」は返却が必要なガロンボトルなので、次のボトルが届くまで空ボトルを保管しておく必要があり、ボトルは再利用されます。ゴミ出し不要なのでエコです。<br />
<br />
ボトルの容量にも違いがあり、12Lボトルは重たいので交換が大変ですが、交換頻度は少なくて済みます。4.3Lや7.2Lの軽量パックは軽いので交換はラクですが、交換頻度が増えます。<br />
<br />
それぞれの<span style="color:#FF0A0A"><strong><strong>メリット・デメリットをよく理解</strong></strong></span>した上で、会社を選びましょう。<br />
<br />
<div class="point5">高機能サーバーか通常サーバーか</div>
一般的な家電と同様にウォーターサーバーによって搭載されている機能は様々なので、<span style="color:#FF0A0A"><u><strong><strong>どのような機能が付いているかを確認</strong></strong></u></span>しましょう。<br />
<br />
「<strong>高機能サーバー</strong>」は、自動でサーバー内部を衛生に保つクリーンシステム、チャイルドロック、静音設計でサーバー音が静か、弱冷水モード、再加熱機能、省エネ機能、ボトルの持ち上げ不要でラクに交換できるボトル下置き型、などの便利な機能が備えられているためおすすめです。<br />
<br />
「<strong>通常サーバー</strong>」は冷水温水が出る、チャイルドロックなどの最低限の機能のみが備わっていますが、サーバーレンタル料が無料または安価なため、費用を抑えたい方は通常サーバーも選択肢の一つになります。<br />
<br />
また、便利な機能性以外にも小さなお子様が安全に使える設計になっているかどうかもチェックしておきたいポイントです。<br />
<br />
<div class="point6">サーバーがご自宅やオフィスに合うかどうか</div>
ウォーターサーバーはインテリアにもなりますので、おしゃれかどうか、ご自宅のリビングやキッチン、またはオフィスに馴染むかどうか、も選ぶ上での重要なポイントです。<br />
<br />
サーバーによっては<span style="color:#FF0A0A"><strong>色を選べる</strong></span>ので、あらかじめ設置予定の場所に合うかどうかを考えて色を選びましょう。サーバーは<span style="color:#FF0A0A"><strong>床置型</strong></span>以外に、台所やテーブルにおける<span style="color:#FF0A0A"><strong>卓上型</strong></span>もあります。<br />
<br />
設置する場所に余裕が無い場合は、<span style="color:#FF0A0A"><strong>コンパクト設計</strong></span>になっているかどうかも確認しましょう。ただし、「ペットボトルを置くスペースが無くなるため、置き場所には困らなかった」という声も。<br />
<br />
<div class="point7">電気代を節約できる省エネ機能が付いているかどうか</div>
ウォーターサーバーは1ヵ月約1000円の電気代がかかりますが、消費電力を節電できる省エネ機能付きのサーバーならば1ヶ月約330円～800円に<span style="color:#FF0A0A"><strong><u>電気代を抑えることができてお得</u></strong></span>です。<br />
<br />
ちなみに、「ウォーターサーバーを設置すると毎月の電気代が増えるのでは？」と思いがちですが、実は電気ポット・電気ケトルでお湯を沸かす分の電気代や、ペットボトルを取り出す時の冷蔵庫の開け閉めでかかる電気代が節約できるため、毎月の電気代は思ったよりも増えないんです。<br />
