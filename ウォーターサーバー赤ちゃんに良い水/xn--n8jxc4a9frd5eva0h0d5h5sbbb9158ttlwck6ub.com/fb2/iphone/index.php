<span class="img-center"><img src="./img/ws_baby2.jpg" /></span>

<p>&nbsp;</p>
<p>赤ちゃんの体の水分比率は大人の体よりも多く、大人は体の約60％が水分ですが、<span style="color:#FF0A0A"><strong>赤ちゃんは体の70～80％が水分</strong></span>だと言われています。</p>
<p>&nbsp;</p>
<p>つまり、<strong>赤ちゃんの体のほとんどは水分</strong>で出来ているんです！</p>
