<p>&nbsp;</p>
<p>しかし、水道水は殺菌のために塩素（カルキ）で消毒をしており、塩素は人間にとって有害な物質です。また、水を塩素処理する過程で発生する発がん性物質のトリハロメタンが含まれています。</p>
<p>&nbsp;</p>
<p>塩素（カルキ）を抜くために水を沸騰させると、沸騰直後はトリハロメタンが多く発生しますので、沸騰させて飲むのも安全とは言えません。</p>
<p>&nbsp;</p>
